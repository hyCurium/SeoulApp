package com.example.jungeb.seoulapp.Items;

public class ListviewFestivalItems {

    private String FestivalTitle;
    private String FestivalAddress;
    private String FestivalKinds;

    public String getFestivalTitle() {
        return FestivalTitle;
    }

    public void setFestivalTitle(String festivalTitle) {
        FestivalTitle = festivalTitle;
    }

    public String getFestivalAddress() {
        return FestivalAddress;
    }

    public void setFestivalAddress(String festivalAddress) {
        FestivalAddress = festivalAddress;
    }

    public String getFestivalKinds() {
        return FestivalKinds;
    }

    public void setFestivalKinds(String festivalKinds) {
        FestivalKinds = festivalKinds;
    }
}
